#include <iostream>
using namespace std;

int main() {
    // Integer variable
    int age = 25;
    
    // Floating-point variable
    double salary = 45000.50;
    
    // Character variable
    char grade = 'A';
    
    // String variable
    string name = "John Doe";
    
    // Boolean variable
    bool isStudent = true;
    
    // Display variables
    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Grade: " << grade << endl;
    cout << "Salary: " << salary << endl;
    cout << "Is Student: " << isStudent << endl;
    
    return 0;
}